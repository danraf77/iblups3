// Directorio de traducciones
const localesDir = path.join(__dirname, 'public', 'locales');

console.log('🌍 Agregando traducciones del perfil a todos los idiomas...');

// Procesar cada idioma
Object.keys(profileTranslations).forEach(lang => {
  const commonPath = path.join(localesDir, lang, 'common.json');
  
  if (fs.existsSync(commonPath)) {
    try {
      // Leer archivo existente
      const content = fs.readFileSync(commonPath, 'utf8');
      const data = JSON.parse(content);
      
      // Agregar traducciones del perfil
      data.profile = profileTranslations[lang];
      
      // Escribir archivo actualizado
      fs.writeFileSync(commonPath, JSON.stringify(data, null, 2));
      console.log(`✅ ${lang}: Traducciones del perfil agregadas`);
    } catch (error) {
      console.error(`❌ Error procesando ${lang}:`, error.message);
    }
  } else {
    console.warn(`⚠️  Archivo no encontrado: ${commonPath}`);
  }
});

console.log('\n🎉 ¡Traducciones del perfil agregadas a todos los idiomas!');
